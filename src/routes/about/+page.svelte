	<h2>Our Mission</h2>
	<p>
		Sorting algorithms are the "Hello World" of algorithm analysis. They introduce crucial concepts
		like Big-O notation, Divide and Conquer, and In-Place sorting.
	</p>
	<p>
		However, static diagrams in textbooks often fail to convey the dynamic nature of how these
		algorithms manipulate memory. SortPedia aims to provide a "Single Source of Truth" for sorting
		knowledge, ensuring that students and enthusiasts have access to:
	</p>
	<ul>
		<li>Accurate, step-by-step visualizations.</li>
		<li>Real-time operation counting (comparisons, swaps, writes).</li>
		<li>Clear, formatted mathematical explanations.</li>
	</ul>
